package basic;

public class Thesis {
	String registration_number, email, title, coding_language, technologies_used, start_date;

	public String getRegistration_number() {
		return registration_number;
	}

	public void setRegistration_number(String registration_number) {
		this.registration_number = registration_number;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getCoding_language() {
		return coding_language;
	}

	public void setCoding_language(String coding_language) {
		this.coding_language = coding_language;
	}

	public String getTechnologies_used() {
		return technologies_used;
	}

	public void setTechnologies_used(String technologies_used) {
		this.technologies_used = technologies_used;
	}

	public String getStart_date() {
		return start_date;
	}

	public void setStart_date(String start_date) {
		this.start_date = start_date;
	}
}
